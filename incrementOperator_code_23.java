public class incrementOperator_code_23 {
 public static void main(String[] args) {
    int a=10;
    //a++ post increment is only for use
    //++a pre increment is increase of the given value
    System.out.println("The post Increment of the given Number is:"+(a++));
    System.out.println("The Pre Increment of the given Number is:"+(++a));
    System.out.println("The post Decrement of the given Number is:"+(a--));
    System.out.println("The Pre Decrement of the given Number is:"+(--a));
 }   
}
